class Solution {
public:
    long long countCommas(long long n) {
        long long res=0;
        if(n<1000) return res;
        if(n>=1000 && n<1000000){
            res=n-999;
        }
        else if(n>=1000000 && n<1000000000){
            res=(999999-999)+(n-999999)*2;
        }
        else if(n>= 1000000000 && n<1000000000000){
            res=(999999-999)+(999999999-999999)*2+(n-999999999)*3;
        }
        else if(n>= 1000000000000 && n<1000000000000000){
            res=(999999-999)+(999999999-999999)*2+(999999999999-999999999)*3+(n-999999999999)*4;
        }
        else{
            res=(999999-999)+(999999999-999999)*2+
                (999999999999-999999999)*3+
                (999999999999999-999999999999)*4+5;
        }
        return res;   
    }
};
